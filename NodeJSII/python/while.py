tracker = 1                    # Initialize the variable 'tracker' with the value 1
while tracker < 10:            # Enter the loop as long as 'tracker' is less than 10
    print(tracker)             # Print the current value of 'tracker'
    tracker = tracker + 1      # Increment the value of 'tracker' by 1 in each iteration

print("Finish loop")           # Print "Finish loop" after the loop is done executing
